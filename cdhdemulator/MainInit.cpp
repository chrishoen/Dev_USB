#include "stdafx.h"

#include "risThreadsProcess.h"
#include "someEmulatorParms.h"

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Initialize program resources.

void main_initialize(int argc,char** argv)
{
   printf("cdhdemulator Program********************************************BEGIN\n");

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Enter process.

   // Set program process for high priority.
   Ris::Threads::enterProcessHigh();

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Initialize print facility.

   // Initialize print.
   Prn::resetPrint();
   Prn::useConsole(1);
   Prn::initializePrint();

   // Initialize print filters.
   Prn::setFilter(Prn::View11, true, 1);
   Prn::setFilter(Prn::View12, false, 1);

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Initialize program resources.

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Read parameters files.

   Some::gEmulatorParms.reset();
   Some::gEmulatorParms.readSection("default");

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   Prn::print(Prn::View11, "cdhdemulator Program*******************************************BEGIN");
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Finalize program resourcs.

void main_finalize()
{
   Prn::print(Prn::View11, "cdhdemulator Program*******************************************END");
   printf("\n");
   printf("cdhdemulator Program********************************************END\n");

   // Finalize print facility.
   Prn::finalizePrint();

   // Exit process.
   Ris::Threads::exitProcess();
}


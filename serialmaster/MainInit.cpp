#include "stdafx.h"

#include "risThreadsProcess.h"
#include "risThreadsThreads.h"

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Initialize program resources.

void main_initialize(int argc,char** argv)
{
   printf("serialecho Program********************************************BEGIN\n");
   printf("serialecho Program********************************************BEGIN\n");
   printf("serialecho Program********************************************BEGIN\n\n");

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Enter process.

   // Set program process for high priority.
   Ris::Threads::enterProcessHigh();

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Initialize program resources.

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Initialize print facility.

   // Initialize print.
   Prn::resetPrint();
   Prn::useConsole(2);
   Prn::initializePrint();

   // Initialize print filters.
   Prn::setFilter(Prn::View21, true, 2);
   Prn::setFilter(Prn::View22, false, 2);

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Initialize program resources.

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   Prn::print(Prn::View11, "serialecho Program********************************************BEGIN");
   Prn::print(Prn::View11, "serialecho Program********************************************BEGIN");
   Prn::print(Prn::View11, "serialecho Program********************************************BEGIN");
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Finalize program resourcs.

void main_finalize()
{
   Prn::print(Prn::View11, "serialecho Program********************************************END");
   printf("\n");
   printf("serialecho Program********************************************END\n");
   printf("serialecho Program********************************************END\n");
   printf("serialecho Program********************************************END\n");

   // Finalize print facility.
   Prn::finalizePrint();

   // Exit process.
   Ris::Threads::exitProcess();
}

